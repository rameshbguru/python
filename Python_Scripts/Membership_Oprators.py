
#!/usr/local/bin/python3.6

#Membership Operators are used to test if a sequence is Present in Object
#in: It return True if the specified value Present in the Sequence
x=[1,2,3]
y=2
z=8
print (y in x)
print (z in x)
print ("**********************")
#not in : It return true if the specified value is not Present in the sequence
print (y not in  x)
print (z not in x)

